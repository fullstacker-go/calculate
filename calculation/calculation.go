package calculation

//a simple fuction which takes two integers as arguement and returns result as addition of them.
func Add(number1, number2 int) int {
	return number1 + number2
}

//a simple fuction which takes two integers as arguement and returns result as substraction of them.
func Substract(number1, number2 int) int {
	return number1 - number2
}

//a simple fuction which takes two integers as arguement and returns result as multiplication of them.
func Multiply(number1, number2 int) int {
	return number1 * number2
}

//a simple fuction which takes two integers as arguement and returns result as division of them.
func Divide(dividend, divisor int) int {
	return dividend / divisor
}
